def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--train-file", required=True)
    parser.add_argument("--val-file", default=None)
    parser.add_argument("--model", default="checkpoints/meta_sft")
    parser.add_argument("--n-gpus", type=int, default=4)
    parser.add_argument("--llm-proxy", action="store_true", default=True)
    args = parser.parse_args()

    # Load dataset
    import pandas as pd
    df = pd.read_parquet(args.train_file)
    problems = df.drop_duplicates("problem_id")
    train_data = [
        MathTask(
            question=row["question"],
            gold_answer=row["gold_answer"],
            problem_id=row["problem_id"],
            category=row["category"],
        )
        for _, row in problems.iterrows()
    ]

    val_data = train_data[:100] if args.val_file is None else None

    print(f"Train: {len(train_data)} problems", flush=True)

    config = verl_config(args.model, n_gpus=args.n_gpus)
    algorithm = agl.VERL(config)

    tracer = agl.OtelTracer()
    adapter = agl.LlmProxyTraceToTriplet()

    trainer = agl.Trainer(
        algorithm=algorithm,
        n_runners=2,
        tracer=tracer,
        adapter=adapter,
        runner=metacot_agent,
        initial_resources={
            "train_dataset": train_data,
            "val_dataset": val_data,
        },
    )

    trainer.fit()


if __name__ == "__main__":
    main()
